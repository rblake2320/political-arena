#!/usr/bin/env node

import { fileURLToPath } from 'node:url';

const DEFAULT_SOURCE = 'data/sources/governor-candidates-2026.json';
const DEFAULT_OUT = 'data/generated/governor-candidates-2026.sql';
const EXPECTED_STATES = 36;

const PARTY_LABELS = {
  Democratic: 'Democrat',
  Republican: 'Republican',
  Independent: 'Independent',
  Libertarian: 'Libertarian',
  Green: 'Green',
  Constitution: 'Constitution',
  'Legal Marijuana Now': 'Legal Marijuana Now',
  'United Citizens': 'United Citizens',
  Workers: 'Workers',
  'Community First': 'Community First',
  'American Independent': 'American Independent',
  Forward: 'Forward',
  'No Labels': 'No Labels',
};

function usage() {
  return `Usage: node scripts/prepare-governor-candidate-2026-loader.mjs [options]

Options:
  --source <path>          Governor candidate source JSON. Default: ${DEFAULT_SOURCE}
  --out <path>             SQL output path. Default: ${DEFAULT_OUT}
  --dry-run                Parse and print counts without writing SQL
  --help                   Show this help

The source JSON is a snapshot of a public candidate list. Rows are written as
source-listed directory entries, not Arena-verified participants.`;
}

function parseArgs(argv) {
  const args = {
    source: DEFAULT_SOURCE,
    out: DEFAULT_OUT,
    dryRun: false,
    help: false,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === '--help') {
      args.help = true;
    } else if (arg === '--dry-run') {
      args.dryRun = true;
    } else if (arg === '--source') {
      args.source = argv[++i];
    } else if (arg === '--out') {
      args.out = argv[++i];
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  return args;
}

function sqlString(value) {
  if (value === null || value === undefined || value === '') return 'NULL';
  return `'${String(value).replaceAll("'", "''")}'`;
}

function slugify(value) {
  return String(value)
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 80);
}

function normalizeParty(party) {
  return PARTY_LABELS[party] || party || 'Unknown';
}

function candidateRowsFromSource(source) {
  if (!source?.source?.url) throw new Error('Source JSON must include source.url');
  if (!Array.isArray(source.races)) throw new Error('Source JSON must include races[]');
  if (source.races.length !== EXPECTED_STATES) {
    throw new Error(`Expected ${EXPECTED_STATES} governor race rows, got ${source.races.length}`);
  }

  const rows = [];
  const seenIds = new Set();
  for (const race of source.races) {
    if (!race.state_code || !race.race_id || !Array.isArray(race.candidates)) {
      throw new Error(`Malformed race row: ${JSON.stringify(race)}`);
    }
    if (race.candidates.length === 0) {
      throw new Error(`${race.state_code} governor race has no candidates in source snapshot`);
    }

    for (const candidate of race.candidates) {
      if (!candidate.name) throw new Error(`${race.state_code} candidate row missing name`);
      const baseId = `cand-gov-2026-${race.state_code.toLowerCase()}-${slugify(candidate.name)}`;
      let id = baseId;
      let suffix = 2;
      while (seenIds.has(id)) {
        id = `${baseId}-${suffix}`;
        suffix += 1;
      }
      seenIds.add(id);

      rows.push({
        id,
        race_id: race.race_id,
        name: candidate.name,
        party: normalizeParty(candidate.party),
        source_status: source.source.status || 'wikipedia_listed',
        source_url: source.source.url,
        source_label: source.source.label || 'Source-listed; not state ballot-certified',
        source_updated_at: source.source.fetched_at || null,
        verification_status: 'pending',
        is_active: 1,
      });
    }
  }

  return rows.sort((a, b) => a.id.localeCompare(b.id));
}

function buildSql(rows, source) {
  const lines = [
    '-- Generated by scripts/prepare-governor-candidate-2026-loader.mjs',
    `-- Source: ${source.source.url}`,
    `-- Source label: ${source.source.label}`,
    '-- Candidate directory rows are source-listed, not Arena-verified participants.',
    '-- Idempotent: unclaimed rows update source fields; claimed rows are not overwritten.',
    '',
  ];

  for (const candidate of rows) {
    lines.push(
      `INSERT INTO candidates (id, race_id, name, party, source_status, source_url, source_label, source_updated_at, verification_status, is_active) VALUES (${[
        candidate.id,
        candidate.race_id,
        candidate.name,
        candidate.party,
        candidate.source_status,
        candidate.source_url,
        candidate.source_label,
        candidate.source_updated_at,
        candidate.verification_status,
        candidate.is_active,
      ].map(sqlString).join(', ')})
ON CONFLICT(id) DO UPDATE SET
  race_id = excluded.race_id,
  name = excluded.name,
  party = excluded.party,
  source_status = excluded.source_status,
  source_url = excluded.source_url,
  source_label = excluded.source_label,
  source_updated_at = excluded.source_updated_at,
  updated_at = datetime('now')
WHERE candidates.user_id IS NULL;`
    );
  }

  lines.push('');
  return lines.join('\n');
}

function summarize(rows, source) {
  const byState = new Map();
  const byParty = new Map();
  for (const row of rows) {
    const state = row.race_id.replace(/^race-2026-/, '').replace(/-GOV$/, '');
    byState.set(state, (byState.get(state) || 0) + 1);
    byParty.set(row.party, (byParty.get(row.party) || 0) + 1);
  }

  return {
    source: source.source.url,
    source_status: source.source.status || 'wikipedia_listed',
    states: byState.size,
    candidates: rows.length,
    by_party: Object.fromEntries([...byParty.entries()].sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))),
    zero_candidate_states: [...byState.entries()].filter(([, count]) => count === 0).map(([state]) => state),
  };
}

async function main() {
  const [fs, path] = await Promise.all([
    import('node:fs/promises'),
    import('node:path'),
  ]);
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    console.log(usage());
    return;
  }

  const source = JSON.parse(await fs.readFile(args.source, 'utf8'));
  const rows = candidateRowsFromSource(source);
  console.log(JSON.stringify(summarize(rows, source), null, 2));

  if (args.dryRun) return;

  await fs.mkdir(path.dirname(args.out), { recursive: true });
  await fs.writeFile(args.out, buildSql(rows, source), 'utf8');
  console.log(`Wrote ${args.out}`);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main().catch(error => {
    console.error(error.message || error);
    process.exit(1);
  });
}

export {
  buildSql,
  candidateRowsFromSource,
  summarize,
};
