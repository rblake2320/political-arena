#!/usr/bin/env node

import { fileURLToPath } from 'node:url';

const DEFAULT_SOURCE = 'data/sources/governors-2026.json';
const DEFAULT_OUT = 'data/generated/governor-2026-races.sql';

function usage() {
  return `Usage: node scripts/prepare-governor-2026-loader.mjs [options]

Options:
  --source <path>          Governor race source JSON. Default: ${DEFAULT_SOURCE}
  --out <path>             SQL output path. Default: ${DEFAULT_OUT}
  --race-status <status>   Race status to write. Default: upcoming
  --dry-run                Parse and print counts without writing SQL
  --help                   Show this help

This script emits state governor race reference rows only. It never executes SQL.`;
}

function parseArgs(argv) {
  const args = {
    source: DEFAULT_SOURCE,
    out: DEFAULT_OUT,
    raceStatus: 'upcoming',
    dryRun: false,
    help: false,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === '--help') {
      args.help = true;
    } else if (arg === '--dry-run') {
      args.dryRun = true;
    } else if (arg === '--source') {
      args.source = argv[++i];
    } else if (arg === '--out') {
      args.out = argv[++i];
    } else if (arg === '--race-status') {
      args.raceStatus = argv[++i];
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  return args;
}

function sqlString(value) {
  if (value === null || value === undefined) return 'NULL';
  return `'${String(value).replaceAll("'", "''")}'`;
}

function parseGovernorSource(source) {
  if (!source || source.cycle !== 2026 || !Array.isArray(source.states)) {
    throw new Error('Governor source must define cycle 2026 and a states array');
  }

  const seen = new Set();
  const races = source.states.map(state => {
    const abbr = String(state.abbr || '').trim().toUpperCase();
    const name = String(state.name || '').trim();
    if (!/^[A-Z]{2}$/.test(abbr) || !name) {
      throw new Error(`Invalid governor state entry: ${JSON.stringify(state)}`);
    }
    if (seen.has(abbr)) throw new Error(`Duplicate governor state entry: ${abbr}`);
    seen.add(abbr);

    return {
      id: `race-${source.cycle}-${abbr}-GOV`,
      name: `${source.cycle} ${name} Governor Race`,
      office: 'Governor',
      state: abbr,
      district: '',
      jurisdiction_level: 'state',
      election_date: source.election_date || '2026-11-03',
      description: `State governor race imported from ${source.source_name}; races-only reference data, not candidate certification.`,
    };
  }).sort((a, b) => a.id.localeCompare(b.id));

  return { races, source };
}

function buildSql(parsed, { raceStatus = 'upcoming' } = {}) {
  const lines = [
    '-- Generated by scripts/prepare-governor-2026-loader.mjs',
    '-- Source label: state_governor_races_only',
    '-- Review before applying. This file is idempotent and uses INSERT OR IGNORE.',
    '',
  ];

  for (const race of parsed.races) {
    lines.push(
      `INSERT OR IGNORE INTO races (id, name, office, state, district, jurisdiction_level, status, election_date, description) VALUES (${[
        race.id,
        race.name,
        race.office,
        race.state,
        race.district,
        race.jurisdiction_level,
        raceStatus,
        race.election_date,
        race.description,
      ].map(sqlString).join(', ')});`
    );
  }

  lines.push('');
  return lines.join('\n');
}

function summarize(parsed) {
  return {
    races: parsed.races.length,
    office: 'Governor',
    jurisdiction_level: 'state',
    election_date: parsed.source.election_date,
    territories_excluded: parsed.source.territories_excluded || [],
    states: parsed.races.map(race => race.state),
  };
}

async function main() {
  const [fs, path] = await Promise.all([
    import('node:fs/promises'),
    import('node:path'),
  ]);
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    console.log(usage());
    return;
  }

  const source = JSON.parse(await fs.readFile(args.source, 'utf8'));
  const parsed = parseGovernorSource(source);
  console.log(JSON.stringify(summarize(parsed), null, 2));

  if (args.dryRun) return;

  const sql = buildSql(parsed, { raceStatus: args.raceStatus });
  await fs.mkdir(path.dirname(args.out), { recursive: true });
  await fs.writeFile(args.out, sql, 'utf8');
  console.log(`Wrote ${args.out}`);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main().catch(error => {
    console.error(error.message || error);
    process.exit(1);
  });
}

export {
  buildSql,
  parseGovernorSource,
  summarize,
};
